set.seed(1)

# Part 1 ------------------------------------------------------------------
x = rbeta(1000, shape1 = 3, shape2 = 5)



# Part 2 ------------------------------------------------------------------
log.lik <- function(prms) {
  shp1 <- prms[1]
  shp2 <- prms[2]
  -sum(dbeta(x, shape1 = shp1, shape2 = shp2, log=TRUE))
}


# Part 3 ------------------------------------------------------------------
opt <- optim(par=c(shape1 = 1, shape2 = 1), log.lik)
opt$par

